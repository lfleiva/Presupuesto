    private void guardarRegistroMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarRegistroMousePressed
        frMensaje.setText("Validando vigencia...");
        if (validarFormulario()) {
            boolean vigenciaExiste = false;
            accesoDatos = new AccesoDatos();                 
            List<Vigencia> listaVigencia = new ArrayList<Vigencia>();

            listaVigencia = accesoDatos.consultarTodos(new Vigencia(), Vigencia.class);

            if (!listaVigencia.isEmpty()) {
                int anioVigencia = frVigencia.getYear();
                for (Vigencia vigenciaIterada : listaVigencia) {
                    if (Integer.toString(anioVigencia).equals(vigenciaIterada.getVigencia())) {                        
                        frMensaje.setText("La vigencia ya existe.");
                        vigenciaExiste = true;
                    }
                }
            }
            
            if(!vigenciaExiste) {
                Vigencia vigenciaNueva = new Vigencia();
                vigenciaNueva.setVigencia(Integer.toString(frVigencia.getYear()));
                vigenciaNueva.setDescripcion(frDescripcion.getText().trim());
                vigenciaNueva.setActiva(false);
                vigenciaNueva = accesoDatos.persistirActualizar(vigenciaNueva);
                administracion_Vigencias.consultarVigencias();
                this.hide();
            }
        } else {
            frMensaje.setText("Formulario incompleto.");
        }        
    }//GEN-LAST:event_guardarRegistroMousePressed

    private boolean validarFormulario() {
        boolean validacionExitosa = true;
        
        if (frDescripcion.getText().trim().equals("")) {
            validacionExitosa = false;
        }

        return validacionExitosa;
    }

    private void reiniciarFormulario() {
        frDescripcion.setText("");
    }

