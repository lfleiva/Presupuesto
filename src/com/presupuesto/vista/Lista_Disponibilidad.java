    private void cargardisponibilidadesRegistradas() {
        accesoDatos = new AccesoDatos();
        List<Disponibilidad> listaDisponibilidads = new ArrayList<Disponibilidad>();
        Disponibilidad disponibilidad = new Disponibilidad();
        disponibilidad.setVigencia(vigencia);
        listaDisponibilidads = accesoDatos.consultarTodosPorVigencia(Disponibilidad.class, vigencia);

        if (!listaDisponibilidads.isEmpty()) {
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) tablaDisponibilidades.getModel();

            for (Disponibilidad disponibilidadIterado : listaDisponibilidads) {                                
                model.addRow(new Object[]{disponibilidadIterado.getConsecutivo(), disponibilidadIterado.getBeneficiario().getNombre(), disponibilidadIterado.getFecha(), disponibilidadIterado.getObjeto()});
            }
        }
    }
    
    /**
     * Metodo que consulta la vigencia activa
     */
    private void consultarVigencia() {
        accesoDatos = new AccesoDatos();
        vigencia = new Vigencia();
        vigencia.setActiva(true);
        vigencia = accesoDatos.consultarTodos(vigencia, Vigencia.class).get(0);
    }
    
